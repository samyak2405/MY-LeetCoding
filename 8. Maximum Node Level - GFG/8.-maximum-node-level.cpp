// Return the level (0-indexed) with maximum number of nodes.

void solve(Node *root,int lvl,int &cnt)
{
    if(!root)
        return;
    if(lvl==1)
    {
        cnt++;
        return;
    }
    else
    {
        solve(root->left,lvl-1,cnt);
        solve(root->right,lvl-1,cnt);
    }
}

int height(Node *root)
{
    return !root?0:1+max(height(root->left),height(root->right));
}
int maxNodeLevel(Node *root)
{
    int h = height(root);
    int lvl = 0;
    int max_cnt = 0;
    for(int i = 1;i<=h;i++)
    {
        int cnt = 0;
        solve(root,i,cnt);
        if(cnt>max_cnt)
        {
            max_cnt = cnt;
            lvl = i-1;
        }
    }
    return lvl;
}