class Solution{
  public:
    Node* findIntersection(Node* head1, Node* head2)
    {
        unordered_set<int> s1;
        Node *p = head2;
        while(p)
        {
            s1.insert(p->data);
            p = p->next;
        }
        Node *head = new Node(0);
        Node *tail = head;
        p = head1;
        while(p)
        {
            if(s1.find(p->data)!=s1.end())
            {
                Node *newnode = new Node(p->data);
                tail->next = newnode;
                tail = newnode;
            }
            p = p->next;
        }
        return head->next;
    }
};

